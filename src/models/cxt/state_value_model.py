# ── Shared helpers ────────────────────────────────────────────────────────────

def compute_possession_cxg(
    actions_df: pd.DataFrame,
    cxg_col: str = "event_cxg",
    possession_id_col: str = "possession_id",
    match_id_col: str = "match_id",
    discount: float = SHOT_DISCOUNT,
) -> pd.Series:
    """
    Compute discounted possession CxG for every action row.

    For each action, possession_cxg = Σ 0.9^k * cxg_k for all shots in
    the same possession (match_id + possession_id), regardless of when
    in the possession the action occurs.

    Returns a Series indexed the same as actions_df.
    """
    group_cols = [c for c in [match_id_col, possession_id_col] if c in actions_df.columns]
    if not group_cols or cxg_col not in actions_df.columns:
        return pd.Series(0.0, index=actions_df.index)

    # Compute discounted possession CxG per (match_id, possession_id)
    poss_cxg: dict = {}
    for key, grp in actions_df.groupby(group_cols):
        shots = grp[grp[cxg_col] > 0][cxg_col].to_numpy()
        if len(shots) == 0:
            val = 0.0
        else:
            val = float(sum(discount**k * cxg for k, cxg in enumerate(shots)))
        poss_cxg[key if len(group_cols) > 1 else key[0]] = val

    def _lookup(row):
        key = tuple(row[c] for c in group_cols) if len(group_cols) > 1 else row[group_cols[0]]
        return poss_cxg.get(key, 0.0)

    return actions_df[group_cols].apply(_lookup, axis=1)


def _make_X(
    df: pd.DataFrame,
    numeric_all: list[str],
    cat_cols: list[str],
    bool_set: frozenset[str],
) -> pd.DataFrame:
    """Build feature matrix from before-state columns."""
    cols = [c for c in numeric_all + cat_cols if c in df.columns]
    X = df[cols].copy()
    for c in bool_set:
        if c in X.columns:
            X[c] = X[c].astype(float)
    return X


def _build_gamma_pipeline(numeric_all: list[str], cat_cols: list[str], alpha: float = 1.0) -> Pipeline:
    from sklearn.preprocessing import OneHotEncoder
    transformers: list = [("num", Pipeline([
        ("imp", SimpleImputer(strategy="median")),
        ("sc", StandardScaler()),
    ]), numeric_all)]
    if cat_cols:
        transformers.append(("cat", Pipeline([
            ("imp", SimpleImputer(strategy="constant", fill_value="unknown")),
            ("ohe", OneHotEncoder(handle_unknown="ignore", sparse_output=False)),
        ]), cat_cols))
    pre = ColumnTransformer(transformers, remainder="drop")
    return Pipeline([("pre", pre), ("reg", TweedieRegressor(power=2, alpha=alpha, max_iter=2000, link="log"))])


def _build_tweedie_pipeline(numeric_all: list[str], cat_cols: list[str], power: float = 1.5, alpha: float = 1.0) -> Pipeline:
    """Tweedie GLM pipeline. power=1.5 handles zero-inflated non-negative targets natively."""
    from sklearn.preprocessing import OneHotEncoder
    transformers: list = [("num", Pipeline([
        ("imp", SimpleImputer(strategy="median")),
        ("sc", StandardScaler()),
    ]), numeric_all)]
    if cat_cols:
        transformers.append(("cat", Pipeline([
            ("imp", SimpleImputer(strategy="constant", fill_value="unknown")),
            ("ohe", OneHotEncoder(handle_unknown="ignore", sparse_output=False)),
        ]), cat_cols))
    pre = ColumnTransformer(transformers, remainder="drop")
    return Pipeline([("pre", pre), ("reg", TweedieRegressor(power=power, alpha=alpha, max_iter=2000, link="log"))])


def _build_tree_reg_pipeline(estimator, numeric_all: list[str], cat_cols: list[str]) -> Pipeline:
    transformers: list = [("num", SimpleImputer(strategy="median"), numeric_all)]
    if cat_cols:
        transformers.append(("cat", Pipeline([
            ("imp", SimpleImputer(strategy="constant", fill_value="unknown")),
            ("enc", OrdinalEncoder(handle_unknown="use_encoded_value", unknown_value=-1, dtype=float)),
        ]), cat_cols))
    pre = ColumnTransformer(transformers, remainder="drop")
    return Pipeline([("pre", pre), ("reg", estimator)])


def _evaluate_regression(y_true: np.ndarray, y_pred: np.ndarray) -> StateValueMetrics:
    mae = float(mean_absolute_error(y_true, y_pred))
    rmse = float(np.sqrt(mean_squared_error(y_true, y_pred)))
    corr, _ = spearmanr(y_true, y_pred)
    spearman = float(corr) if not np.isnan(corr) else None
    # Calibration by zone (quartiles on positive targets only)
    buckets: dict[str, float] = {}
    try:
        pos_mask = y_true > 0
        if pos_mask.sum() >= 4:
            q = np.quantile(y_true[pos_mask], [0.25, 0.5, 0.75])
            edges = [0.0] + q.tolist() + [float("inf")]
            for i in range(len(edges) - 1):
                mask = (y_true >= edges[i]) & (y_true < edges[i + 1])
                if mask.sum() > 0:
                    buckets[f"q{i+1}"] = float(np.mean(y_pred[mask]) - np.mean(y_true[mask]))
    except Exception:
        pass
    return StateValueMetrics(mae=mae, rmse=rmse, spearman=spearman, calibration_by_zone=buckets)


# ── Base class ────────────────────────────────────────────────────────────────

class _BaseStateValueModel:
    feature_set: CxTFeatureSetSpec
    _numeric_all: list[str]
    _cat_cols: list[str]
    _bool_set: frozenset[str]
    pipeline: Pipeline | None

    def _resolve_cols(self, df: pd.DataFrame) -> tuple[list[str], list[str]]:
        numeric_all = [c for c in self.feature_set.numeric_all if c in df.columns]
        cat_cols = [c for c in self.feature_set.categorical if c in df.columns]
        return numeric_all, cat_cols

    def _X(self, df: pd.DataFrame) -> pd.DataFrame:
        return _make_X(df, self._numeric_all, self._cat_cols, self._bool_set)

    def predict(self, actions_df: pd.DataFrame) -> np.ndarray:
        if self.pipeline is None:
            raise RuntimeError("Model not fitted. Call fit() first.")
        return np.clip(self.pipeline.predict(self._X(actions_df)), 0.0, None)

    def evaluate(self, actions_df: pd.DataFrame, target_col: str = "possession_cxg") -> StateValueMetrics:
        y = actions_df[target_col].astype(float).to_numpy()
        p = self.predict(actions_df)
        return _evaluate_regression(y, p)

    def save(self, path: str | Path) -> None:
        Path(path).parent.mkdir(parents=True, exist_ok=True)
        with open(path, "wb") as f:
            pickle.dump(self, f)

    @classmethod
    def load(cls, path: str | Path):
        with open(path, "rb") as f:
            return pickle.load(f)


# ── Gamma GLM (positive targets only) ────────────────────────────────────────

class GammaStateValueModel(_BaseStateValueModel):
    """Gamma GLM state-value model. Requires strictly positive target values."""

    def __init__(
        self,
        feature_set: str | CxTFeatureSetSpec = "contextual",
        alpha: float = 1.0,
        random_state: int = 42,
    ) -> None:
        self.feature_set = get_feature_set(feature_set) if isinstance(feature_set, str) else feature_set
        self.alpha = alpha
        self.random_state = random_state
        self.pipeline: Pipeline | None = None
        self._numeric_all: list[str] = []
        self._cat_cols: list[str] = []
        self._bool_set: frozenset[str] = frozenset()

    def fit(self, actions_df: pd.DataFrame, target_col: str = "possession_cxg") -> "GammaStateValueModel":
        if actions_df.empty:
            raise ValueError("actions_df is empty")
        if target_col not in actions_df.columns:
            raise ValueError(f"Missing target column: {target_col!r}")
        y = actions_df[target_col].astype(float)
        if (y <= 0).any():
            raise ValueError(
                "GammaStateValueModel requires strictly positive targets. "
                "Filter rows with possession_cxg <= 0 before fitting."
            )
        numeric_all, cat_cols = self._resolve_cols(actions_df)
        if not numeric_all:
            raise ValueError("No numeric feature columns found")
        self._numeric_all = numeric_all
        self._cat_cols = cat_cols
        self._bool_set = frozenset(c for c in self.feature_set.boolean if c in numeric_all)
        self.pipeline = _build_gamma_pipeline(numeric_all, cat_cols, self.alpha)
        self.pipeline.fit(self._X(actions_df), y.to_numpy())
        return self


