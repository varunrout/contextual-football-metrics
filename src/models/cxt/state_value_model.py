# ── FFNN ──────────────────────────────────────────────────────────────────────

class FFNNStateValueModel(_BaseStateValueModel):
    """
    Feed-forward neural network state-value model with Huber loss.

    Architecture: Linear(d_in → 256) → ReLU → Dropout(0.1) →
                  Linear(256 → 128) → ReLU → Dropout(0.1) → Linear(128 → 1) → Softplus
    """

    def __init__(
        self,
        feature_set: str | CxTFeatureSetSpec = "contextual",
        hidden_dims: tuple[int, ...] = (256, 128),
        lr: float = 1e-3,
        max_epochs: int = 50,
        batch_size: int = 256,
        huber_delta: float = 0.1,
        random_state: int = 42,
    ) -> None:
        self.feature_set = get_feature_set(feature_set) if isinstance(feature_set, str) else feature_set
        self.hidden_dims = hidden_dims
        self.lr = lr
        self.max_epochs = max_epochs
        self.batch_size = batch_size
        self.huber_delta = huber_delta
        self.random_state = random_state
        self.pipeline: Pipeline | None = None  # scaler
        self._torch_model = None
        self._numeric_all: list[str] = []
        self._cat_cols: list[str] = []
        self._bool_set: frozenset[str] = frozenset()

    def _build_torch_model(self, in_dim: int):
        try:
            import torch.nn as nn
        except ImportError as exc:
            raise ImportError("PyTorch not installed.") from exc

        layers: list = []
        prev = in_dim
        for h in self.hidden_dims:
            layers += [nn.Linear(prev, h), nn.ReLU(), nn.Dropout(0.1)]
            prev = h
        layers += [nn.Linear(prev, 1), nn.Softplus()]

        class _MLP(nn.Module):
            def __init__(self, layers):
                super().__init__()
                self.net = nn.Sequential(*layers)
            def forward(self, x):
                return self.net(x).squeeze(-1)

        return _MLP(layers)

    def fit(self, actions_df: pd.DataFrame, target_col: str = "possession_cxg") -> "FFNNStateValueModel":
        try:
            import torch
            import torch.nn as nn
            from torch.utils.data import DataLoader, TensorDataset
        except ImportError as exc:
            raise ImportError("PyTorch not installed. Run: pip install torch") from exc

        if actions_df.empty:
            raise ValueError("actions_df is empty")
        if target_col not in actions_df.columns:
            raise ValueError(f"Missing target column: {target_col!r}")

        torch.manual_seed(self.random_state)
        df = actions_df.reset_index(drop=True)
        numeric_all, _ = self._resolve_cols(df)
        if not numeric_all:
            raise ValueError("No feature columns found")
        self._numeric_all = numeric_all
        self._cat_cols = []  # FFNN uses numeric only
        self._bool_set = frozenset(c for c in self.feature_set.boolean if c in numeric_all)

        from sklearn.pipeline import Pipeline as SKPipeline
        self.pipeline = SKPipeline([
            ("imp", SimpleImputer(strategy="median")),
            ("sc", StandardScaler()),
        ])
        X_raw = _make_X(df, self._numeric_all, [], self._bool_set)[self._numeric_all]
        X_np = self.pipeline.fit_transform(X_raw).astype(np.float32)
        y_np = df[target_col].astype(float).to_numpy(dtype=np.float32)

        dataset = TensorDataset(torch.tensor(X_np), torch.tensor(y_np))
        loader = DataLoader(dataset, batch_size=self.batch_size, shuffle=True)
        model = self._build_torch_model(X_np.shape[1])
        optimizer = torch.optim.AdamW(model.parameters(), lr=self.lr, weight_decay=1e-4)
        criterion = nn.HuberLoss(delta=self.huber_delta)
        model.train()

        for epoch in range(self.max_epochs):
            epoch_loss = 0.0
            for X_batch, y_batch in loader:
                optimizer.zero_grad()
                pred = model(X_batch)
                loss = criterion(pred, y_batch)
                loss.backward()
                torch.nn.utils.clip_grad_norm_(model.parameters(), 1.0)
                optimizer.step()
                epoch_loss += loss.item()
            if (epoch + 1) % 10 == 0:
                logger.info("FFNNStateValue epoch %d/%d loss=%.4f",
                            epoch + 1, self.max_epochs, epoch_loss / max(len(loader), 1))

        model.eval()
        self._torch_model = model
        return self

    def predict(self, actions_df: pd.DataFrame) -> np.ndarray:
        try:
            import torch
        except ImportError as exc:
            raise ImportError("PyTorch not installed") from exc
        if self._torch_model is None:
            raise RuntimeError("Model not fitted. Call fit() first.")
        df = actions_df.reset_index(drop=True)
        X_raw = _make_X(df, self._numeric_all, [], self._bool_set)[self._numeric_all]
        X_np = self.pipeline.transform(X_raw).astype(np.float32)
        self._torch_model.eval()
        with torch.no_grad():
            out = self._torch_model(torch.tensor(X_np)).numpy()
        return np.clip(out, 0.0, None)


