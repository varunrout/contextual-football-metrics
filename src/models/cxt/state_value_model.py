"""
State-value model for CxT — Phase 7b.

Target: possession_cxg — the total (discounted) CxG generated by the
        possessing team from all shots in the same possession.

Discount factor 0.9 per shot index (shots later in the possession are
downweighted) to reward actions that more directly precede goal threat.

        possession_cxg = Σ_{k} 0.9^k * cxg_k
        where k = shot index within the possession (0-indexed)

This is a regression on a non-negative target. Possessions that do not
generate a shot have target = 0.

Model ladder (all regressors):
  1. gamma_glm          — Gamma GLM (log link); requires target > 0
  2. xgb_contextual     — XGBoost regressor
  3. lgbm_contextual    — LightGBM regressor
  4. ffnn_contextual    — Feed-forward neural network with Huber loss

Evaluation: MAE, RMSE, Spearman rank correlation, calibration by zone bucket.
"""

